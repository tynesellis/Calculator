using System;

namespace Calculator

{

    public class Calculator

    {
        public int Add(int valA, int valB) => valA + valB;
        public int Subtract(int valA, int valB) => valA - valB;
        public int Multiply(int valA, int valB) => valA * valB;
        public int Divide(int valA, int valB) => valA / valB;

    }
}